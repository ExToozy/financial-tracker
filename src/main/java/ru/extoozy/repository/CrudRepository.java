package ru.extoozy.repository;

import java.util.List;

/**
 * Интерфейс для работы с основными CRUD-операциями (создание, чтение, обновление, удаление)
 * для сущности типа E с идентификатором типа I.
 *
 * @param <E> тип сущности
 * @param <I> тип идентификатора сущности
 */
public interface CrudRepository<E, I> {

    /**
     * Сохраняет указанную сущность в репозитории.
     *
     * @param entity сущность, которую нужно сохранить
     */
    void save(E entity);

    /**
     * Обновляет существующую сущность в репозитории.
     *
     * @param entity сущность, которую нужно обновить
     */
    void update(E entity);

    /**
     * Удаляет сущность по указанному идентификатору.
     *
     * @param id идентификатор сущности, которую нужно удалить
     * @return {@code true} если сущность была удалена, {@code false} если сущность не найдена
     */
    boolean delete(I id);

    /**
     * Возвращает все сущности из репозитория.
     *
     * @return список всех сущностей
     */
    List<E> findAll();

    /**
     * Находит сущность по указанному идентификатору.
     *
     * @param id идентификатор сущности
     * @return сущность, соответствующая указанному идентификатору,
     * или {@code null}, если сущность не найдена
     */
    E findById(I id);
}
